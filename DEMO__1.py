class Deck(object):
    def __init__(self):
        self.cards = []
        self.build()
    
    def build(self):
        self.cards =[]
        for val in range(1,23): #ไม่งั้นไพ่ใบที่สองไม่แสดง
            self.cards.append(Tarrot_Cards(val))
    
    def show(self):
        for card in self.cards:
            print (card.show())
    
    def shuffle(self, num=1):
        length = len(self.cards)
        for _ in range (num):
            for i in range( length -1,0,-1):
                randi = random.randint(0,i) #import random
                if i == randi:
                        continue
                self.cards[i] , self.cards[randi] = self.cards[randi] , self.cards[i]

    def deal(self):
        return self.cards.pop()

