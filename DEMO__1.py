class Owners(object):
    def __init__(self,name):
        self.name = name
        self.hand = []
    
    def Have_A_Good_Day(self):
        print ( " Have A Good Day!!, {}".format(self.name))
        return self

    def draw(self,Deck,num=1):
        for _ in range(num):
            card = Deck.deal()
            if card:
                self.hand.append(card)
            else:
                return False
        return True

    def showHand(self):
        print("{}'s hand: {} ".format(self.name,self.hand))
        return self

